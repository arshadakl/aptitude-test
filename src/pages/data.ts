const questionsBySection = questions.reduce((acc, question) => {
  let sectionName: string;
  let sectionTitle: string ;

  if (question.level === 'aptitudeLevel') {
    sectionName = 'aptitudeLevel';
    sectionTitle = 'Aptitude Level';
  } else if (question.level === 'InterestPersonality') {
    sectionName = 'InterestPersonality';
    sectionTitle = 'Interest Personality';
  } else if (question.level === 'SubjectKnowledge') {
    sectionName = 'SubjectKnowledge';
    sectionTitle = 'Subject Knowledge';
  } else {
    sectionName = 'Other';
    sectionTitle = 'Other Section'; // You can add a title for 'Other' as well if needed
  }

  const existingSection = acc.find(section => section.section === sectionName);

  if (existingSection) {
    existingSection.questions.push(question);
  } else {
    acc.push({
      section: sectionName,
      sectionTitle, // Add the new title field here
      questions: [question],
    });
  }

  return acc;
}, [] as { section: string; sectionTitle?: string; questions: Question[] }[]);

